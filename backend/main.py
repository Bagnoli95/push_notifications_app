# Conexi√≥n a Oracle
@contextmanager
def get_db_connection():
    connection = None
    try:
        connection = cx_Oracle.connect(ORACLE_USER, ORACLE_PASSWORD, ORACLE_DSN)
        yield connection
    except cx_Oracle.Error as e:
        print(f"Oracle connection error: {e}")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Database connection failed"
        )
    finally:
        if connection:
            connection.close()

# Funciones de utilidad
def hash_password(password: str) -> str:
    return bcrypt.hashpw(password.encode('utf-8'), bcrypt.gensalt()).decode('utf-8')

def verify_password(password: str, hashed: str) -> bool:
    return bcrypt.checkpw(password.encode('utf-8'), hashed.encode('utf-8'))

def create_access_token(data: dict):
    to_encode = data.copy()
    expire = datetime.utcnow() + timedelta(minutes=ACCESS_TOKEN_EXPIRE_MINUTES)
    to_encode.update({"exp": expire})
    encoded_jwt = jwt.encode(to_encode, SECRET_KEY, algorithm=ALGORITHM)
    return encoded_jwt

def verify_token(credentials: HTTPAuthorizationCredentials = Depends(security)):
    try:
        payload = jwt.decode(credentials.credentials, SECRET_KEY, algorithms=[ALGORITHM])
        username: str = payload.get("sub")
        if username is None:
            raise HTTPException(
                status_code=status.HTTP_401_UNAUTHORIZED,
                detail="Could not validate credentials"
            )
        return payload
    except jwt.PyJWTError:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Could not validate credentials"
        )

# Inicializar tablas
def init_database():
    try:
        with get_db_connection() as conn:
            cursor = conn.cursor()
            
            # Tabla usuarios - manejo de errores para Oracle
            try:
                cursor.execute("""
                    CREATE TABLE users (
                        id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        username VARCHAR2(50) UNIQUE NOT NULL,
                        email VARCHAR2(100) UNIQUE NOT NULL,
                        password_hash VARCHAR2(255) NOT NULL,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                """)
                print("‚úÖ Table 'users' created")
            except cx_Oracle.DatabaseError as e:
                if "ORA-00955" in str(e):  # Table already exists
                    print("‚ÑπÔ∏è Table 'users' already exists")
                else:
                    raise
            
            # Tabla devices
            try:
                cursor.execute("""
                    CREATE TABLE devices (
                        id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        user_id NUMBER REFERENCES users(id),
                        device_id VARCHAR2(255) NOT NULL,
                        fcm_token VARCHAR2(500) NOT NULL,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                        updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                """)
                print("‚úÖ Table 'devices' created")
            except cx_Oracle.DatabaseError as e:
                if "ORA-00955" in str(e):
                    print("‚ÑπÔ∏è Table 'devices' already exists")
                else:
                    raise
            
            # Tabla notificaciones internas
            try:
                cursor.execute("""
                    CREATE TABLE internal_notifications (
                        id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        user_id NUMBER REFERENCES users(id),
                        title VARCHAR2(255) NOT NULL,
                        message CLOB NOT NULL,
                        is_read NUMBER(1) DEFAULT 0,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                """)
                print("‚úÖ Table 'internal_notifications' created")
            except cx_Oracle.DatabaseError as e:
                if "ORA-00955" in str(e):
                    print("‚ÑπÔ∏è Table 'internal_notifications' already exists")
                else:
                    raise
            
            conn.commit()
            print("‚úÖ Database initialization completed")
            
    except Exception as e:
        print(f"‚ùå Database initialization failed: {e}")
        if ENVIRONMENT == "development":
            print("‚ÑπÔ∏è Continuing in development mode...")
        else:
            raise

